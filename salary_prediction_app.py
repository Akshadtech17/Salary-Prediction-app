    # Evaluate on the transformed scale
    mae_scaled_trans = mean_absolute_error(y_test_trans, y_pred_scaled_trans)
    r2_scaled_trans = r2_score(y_test_trans, y_pred_scaled_trans)

    results_scaled_transformed[name] = {'MAE_scaled_transformed': mae_scaled_trans, 'R2_scaled_transformed': r2_scaled_trans}

    print(f"{name} MAE (scaled & transformed): {mae_scaled_trans:.4f}")
