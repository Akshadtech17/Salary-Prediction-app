"""### Logarithmic Transformation of 'Salary'

The distribution of 'Salary' is often skewed, with a long tail towards higher values. This can negatively impact the performance of many regression models. Applying a logarithmic transformation (e.g., `np.log1p`) can help make the distribution more symmetrical and reduce the impact of outliers, leading to better model training. After transformation, we will re-train and evaluate the models.
"""

import numpy as np

# Apply log1p transformation to the target variable 'y'
y_transformed = np.log1p(df['Salary'])

# Split the transformed data
X_train_trans, X_temp_trans, y_train_trans, y_temp_trans = train_test_split(X, y_transformed, test_size=0.3, random_state=42)
X_val_trans, X_test_trans, y_val_trans, y_test_trans = train_test_split(X_temp_trans, y_temp_trans, test_size=0.5, random_state=42)

print(f"Transformed y_train shape: {y_train_trans.shape}")
print(f"Transformed y_val shape: {y_val_trans.shape}")
print(f"Transformed y_test shape: {y_test_trans.shape}")

"""### Re-evaluating Models with Transformed Target Variable

Now, let's re-train the same set of models using the logarithmically transformed 'Salary' as the target variable and observe the changes in MAE and R2 scores. Remember that MAE will now be in the scale of the transformed values, and R2 is a more reliable indicator of improvement in this context.
"""

# Initialize models again (or reuse existing 'models' dict)
# Train and evaluate each model with transformed target
results_transformed = {}
for name, model in models.items():
    print(f"\n--- Training {name} with Transformed Target ---")
    model.fit(X_train_trans, y_train_trans)
    y_pred_trans = model.predict(X_test_trans)

    # Note: MAE will be on the log scale now, R2 is scale-independent
    mae_trans = mean_absolute_error(y_test_trans, y_pred_trans)
    r2_trans = r2_score(y_test_trans, y_pred_trans)

    results_transformed[name] = {'MAE_transformed': mae_trans, 'R2_transformed': r2_trans}

    print(f"{name} MAE (transformed): {mae_trans:.4f}")
    print(f"{name} R2 Score (transformed): {r2_trans:.4f}")

